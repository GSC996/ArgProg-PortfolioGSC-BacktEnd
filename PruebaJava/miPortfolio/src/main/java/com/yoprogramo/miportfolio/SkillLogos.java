
package com.yoprogramo.miportfolio;

public class SkillLogos {
     
    private String urlLogo;
    private String textAlt;

    public SkillLogos(String urlLogo, String textAlt) {
        this.urlLogo = urlLogo;
        this.textAlt = textAlt;
    }

    public String getUrlLogo() {
        return urlLogo;
    }

    public void setUrlLogo(String urlLogo) {
        this.urlLogo = urlLogo;
    }

    public String getTextAlt() {
        return textAlt;
    }

    public void setTextAlt(String textAlt) {
        this.textAlt = textAlt;
    }
    
    
}
